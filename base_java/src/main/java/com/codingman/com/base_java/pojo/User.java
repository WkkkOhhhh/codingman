package com.codingman.com.base_java.pojo;

public class User {
private String name;
private String sexy;
public User(String name, String sexy) {
	super();
	this.name = name;
	this.sexy = sexy;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getSexy() {
	return sexy;
}
public void setSexy(String sexy) {
	this.sexy = sexy;
}
}
